async function main() {
  for (const t of TEMPLATES) {
    // 既存チェック
    const { data: existing } = await sb.from('buzz_templates')
      .select('id, code').eq('code', t.code).maybeSingle();
    if (existing) {
      const { error } = await sb.from('buzz_templates').update(t).eq('id', existing.id);
      if (error) console.error(`[ERR] ${t.code}`, error);
      else console.log(`[UPDATED] ${t.code} — ${t.name}`);
    } else {
      const { error } = await sb.from('buzz_templates').insert(t);
      if (error) console.error(`[ERR] ${t.code}`, error);
      else console.log(`[INSERTED] ${t.code} — ${t.name}`);
    }
  }

  // 確認
  const { data: all } = await sb.from('buzz_templates').select('code, name, is_active, tags').order('code');
  console.log(`\n=== buzz_templates total: ${all?.length} ===`);
  for (const t of all ?? []) {
    const isTree = (t.tags ?? []).some(x => x === 'ツリー必須' || x === 'ツリー推奨' || x === 'ツリー可');
    const marker = isTree ? '🌳' : '  ';
    console.log(`${marker} ${t.code.padEnd(24)} ${t.name}`);
  }
}

main().catch(e => { console.error(e); process.exit(1); });
